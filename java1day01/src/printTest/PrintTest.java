/* 'Hello Java' 메세지를 출력하는 프로그램*/ //DATE: 2024.01.31.

public class PrintTest {
	public static void main(String[] args) {
		
		System.out.print("Hello Java");// 세미 클론;은 끝이라는 컴퓨터 언어
		System.out.println("Hello Garam");
		//println 이라는 함수는 소괄호 안에 전달해준 값을 콘솔창에 출력해주는 기능을 가지고 있다.
		// syso + ctrl + space => 출력 단축키
		System.out.println("얼음골 사과 먹어야지~");
		
		
/* 출력 코드의 형태 */
		
// 1)그냥 줄바꿈		
		System.out.println();		
// 2)다른 형태가 하나 더 있음.		
		System.out.println(" ");

		
		
	}// 메인함수 중괄호
}// 패키지 중괄호


