package operator;

public class Oper5 {
	public static void main(String[] args) {
		int data = 10;
		
		
		System.out.println(data); //10
		System.out.println(data++); //10
		System.out.println(data++); // 11
		System.out.println(data); // 12
		System.out.println(++data); //13
		System.out.println(data); //13
	}
}
